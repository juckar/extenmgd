import datetime

#================================================================================================================================================
def c2date( cad ) :
    #   06_Factura_FechaDoc : 20100101
    #   07_Factura_FechaAsien : 20100101
    #   08_Factura_FechaValor : 20100101
    
    d = int(cad[6:])
    m = int(cad[4:6])
    a = int(cad[:4])
    
    return datetime.datetime( a, m, d )
    
#================================================================================================================================================
def cuenta( cta, tamcuenta ) :
    cta = cta.strip()
    ncta = len(cta)
    cta1 = cta[:3]
    if ncta < tamcuenta :
        cta = cta1 + "0"*(tamcuenta-ncta) + cta[3:]
    elif ncta > tamcuenta :
        cta = cta1 + cta[(3-tamcuenta):]
    return cta
    
